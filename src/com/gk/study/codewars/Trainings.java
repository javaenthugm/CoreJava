	private static boolean isIncDecr(BigInteger x){
		String s = String.valueOf(x);
		if(s.length()==1){
			return true;
		}
		
		boolean isIncrease=false,isDecrease = false,neigther=false;
		for(int i=0;i<s.length();i++){
			if(isIncrease){
				if(Integer.valueOf(String.valueOf(s.charAt(i))) < Integer.valueOf(String.valueOf(s.charAt(i-1)))){
					isIncrease=false;
					neigther=false;
					break;
				}
			}
			if(isDecrease ){
 				if(Integer.valueOf(String.valueOf(s.charAt(i))) > Integer.valueOf(String.valueOf(s.charAt(i-1)))){
					isDecrease=false;
					neigther=false;
					break;
				}
			}
			if(neigther ){
				if(Integer.valueOf(String.valueOf(s.charAt(i))) != Integer.valueOf(String.valueOf(s.charAt(i-1)))){
					if(Integer.valueOf(String.valueOf(s.charAt(i))) < Integer.valueOf(String.valueOf(s.charAt(i-1)))){
						isDecrease=true;
					}else if(Integer.valueOf(String.valueOf(s.charAt(i))) > Integer.valueOf(String.valueOf(s.charAt(i-1)))){
						isIncrease=true;
					}
					neigther=false;
				}
			}
			if(i>0){
				if(Integer.valueOf(String.valueOf(s.charAt(i))) > Integer.valueOf(String.valueOf(s.charAt(i-1)))){
					isIncrease=true;
				}else if(Integer.valueOf(String.valueOf(s.charAt(i))) < Integer.valueOf(String.valueOf(s.charAt(i-1)))){
					isDecrease=true;
				}else if(Integer.valueOf(String.valueOf(s.charAt(i))) == Integer.valueOf(String.valueOf(s.charAt(i-1)))){
					neigther=true;
				}
			}
		}

		return isIncrease||isDecrease||neigther;
		
	}
	
