	public static boolean groupCheck(String s){
	    Stack<String> stack = new Stack<>();
	    for(int i=0;i<s.length();i++){
	    	String pkVal = String.valueOf(s.charAt(i));
	    	if("[".equals(pkVal) || "{".equals(pkVal)||"(".equals(pkVal)){
		    	stack.push(pkVal);
		    }else if(")".equals(pkVal)){
		    	if("(".equals(stack.peek())){
		    		stack.pop();
		    	}
		    }else if("}".equals(pkVal)){
		    	if("{".equals(stack.peek())){
		    		stack.pop();
		    	}
		    }else if("]".equals(pkVal)){
		    	if("[".equals(stack.peek())){
		    		stack.pop();
		    	}
		    }
	    }
		return stack.empty();
	}
	
