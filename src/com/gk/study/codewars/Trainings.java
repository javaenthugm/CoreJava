	public static String getWinner(final List<String> listOfBallots) {
		if(listOfBallots.size()==0 || listOfBallots==null){
			return null;
		}
	
		int winCount = listOfBallots.size()/2;
		
		Map<String, Integer> candCount = new HashMap<>();
		
		for(String s : listOfBallots){
			if(candCount.containsKey(s)){
				candCount.put(s, candCount.get(s)+1);
			}else{
				candCount.put(s,Integer.valueOf(1));
			}
			
			if(candCount.get(s)>winCount){
				return s;
			}
		}
		return null;
	}
	
