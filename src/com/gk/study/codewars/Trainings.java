	public static int[][] pascal(int depth) {
		int[][] pA = new int[depth][];
		pA[0] = new int[]{1};
		for(int i=1;i<depth;i++){
    		int[] temp = new int[i+1];
    		temp[0] = 1;
    		temp[temp.length-1]=1;
    		for(int j=0;j<pA[i-1].length-1;j++){
	    		temp[j+1]=pA[i-1][j]+pA[i-1][j+1];
	    	}
    		pA[i]=temp;
    	}
    	return pA;
	}
	
	public static BigInteger perimeter(BigInteger n) {
		BigInteger totPerimeter = BigInteger.valueOf(0);
		
		for(BigInteger i:specialFibonacci(n)){
			totPerimeter=totPerimeter.add(i);
		}
		return totPerimeter.multiply(BigInteger.valueOf(4));
	}
	
	private static List<BigInteger> specialFibonacci(BigInteger n){
		if(n.intValue()<0){
			throw new IllegalArgumentException("n must not be less than zero");
		}
		if(n.intValue()==1){
			return Arrays.asList(BigInteger.valueOf(1),BigInteger.valueOf(1));
		}
		
		List<BigInteger> seq = new ArrayList<>(n.intValue());
		seq.add(BigInteger.valueOf(1));
		n =  n.subtract(BigInteger.valueOf(1));
		seq.add(BigInteger.valueOf(1));
		n = n.subtract(BigInteger.valueOf(1));
		
		while(n.intValue()>=0){
			BigInteger a = seq.get(seq.size()-1);
			BigInteger b = seq.get(seq.size()-2);
		
			seq.add(a.add(b));
			n= n.subtract(BigInteger.valueOf(1));
		}
		
		return seq;
	}
	
